    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $_size = SizeProductQty::findorFail($id);

        $_size->stock = $request->input("size".$_size->size);
//        dd($_size);
        $_size->save();
        return redirect()->route('admin.product.edit', ['id' => $_size->product_id]);
    }

