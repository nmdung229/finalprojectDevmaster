    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
        $product = Product::findorFail($id);
        $data = SizeProductQty::where(['product_id' => $id])->get();
        return view('admin.product.sizeProduct', [
           'product' => $product,
           'data' => $data,
        ]);
    }

